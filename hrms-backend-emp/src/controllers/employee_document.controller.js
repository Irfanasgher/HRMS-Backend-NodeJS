exports.create = async (req, res) => {
  // Create comment
  const addressType = {
    employee_id: req.body.employee_id,
    document_type_id: req.body.document_type_id,
    title_employee_document: req.body.title_employee_document,
    description_employee_document: req.body.description_employee_document,
    url_employee_document: req.body.url_employee_document,

    is_enable_employee_document: 1,
  };
  await db.employee_document
    .create(addressType)
    .then((data) => {
      res.send(data);
    })
    .catch((err) => {
      res.status(500).send({
        message: err.message || "addressType cant be created",
      });
    });
};

